package GeneralFunctions;

public class GeneralFunctions {

    public static void printArray(Object[] array){
        System.out.print("[ ");
        for (Object o : array) {
            System.out.print(o.toString() + " ");
        }
        System.out.println("]");
    }
    public static void printArray(int[] array){
        System.out.print("[ ");
        for (Object o : array) {
            System.out.print(o.toString() + " ");
        }
        System.out.println("]");
    }




}
